def print_circum(radius):
    """Prints the circumference of a circle given its radius."""
    pi = 3.14159
    circumference = 2 * pi * radius
    print(f"Radius: {radius} -> Circumference: {circumference}")
# Three function calls with different radii
print_circum(1)
print_circum(2.5)
print_circum(10)

